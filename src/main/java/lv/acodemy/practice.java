package lv.acodemy;

public class practice {
    public static void main(String[] args) {

        //write a for loop that prints numbers from 1 to 10 on separate lines
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        //using for loop, print all even numbers from 2 to 20
        for (int j = 2; j <= 20; j+=2) {
            System.out.println(j);
        }

        //using for loop to print the multiplication table of 5 up to 5 * 10
        for (int k = 1; k <= 10; k++) {
            System.out.println(5*k);
        }
    }
}
